function getSavedLang() {
  return localStorage.getItem('porra_lang') || 'ca';
}

// Expose supported langs
const SUPPORTED_LANGS = ['ca', 'es', 'en'];

function setLanguage(lang) {
  if (!translations[lang]) lang = 'ca';
  localStorage.setItem('porra_lang', lang);

  document.documentElement.lang = lang;
  
  // Select active dropdown option
  document.querySelectorAll('.lang-selector').forEach(sel => {
    sel.value = lang;
  });

  const elements = document.querySelectorAll('[data-i18n], [data-i18n-title], [data-i18n-placeholder]');
  elements.forEach(el => {
    if (el.hasAttribute('data-i18n')) {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
        // Use innerHTML so that elements with <br/> or <strong> work
        el.innerHTML = translations[lang][key];
        }
    }
    if (el.hasAttribute('data-i18n-title')) {
        const key = el.getAttribute('data-i18n-title');
        if (translations[lang][key]) {
            el.title = translations[lang][key];
        }
    }
    if (el.hasAttribute('data-i18n-placeholder')) {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    }
  });

  // Notify the page so it can re-render dynamic content
  document.dispatchEvent(new CustomEvent('langchange', { detail: { lang } }));
}

function t(key) {
  const lang = getSavedLang();
  return translations[lang] && translations[lang][key] ? translations[lang][key] : (translations['ca'][key] || key);
}

// Initialize on load
document.addEventListener('DOMContentLoaded', () => {
  const lang = getSavedLang();
  setLanguage(lang);
  
  // Add listeners to all dropdowns
  document.querySelectorAll('.lang-selector').forEach(sel => {
    sel.addEventListener('change', (e) => {
      setLanguage(e.target.value);
    });
  });
});
